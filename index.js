function add(a,b) {
  var sum = a + b;
  return sum;
}

function subtract(a,b) {
  var sum = a - b;
  return sum;
}

function multiply(a,b) {
  var sum = a * b;
  return sum;
}

function divide(a,b) {
  var sum = a / b;
  return sum;
}

function inc(n) {
  n += 1;
  return n;
}

function dec(n) {
  n -= 1;
  return n;
}

function makeInt(n) {
  parseInt(n,10);
}